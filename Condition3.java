// If Else leder Condition Program 

class Condition3
{
	public static void main(String[] args) 
	{
		char c = 'A';
		if(c == 'A')
		{
			System.out.println("You Select A");
		}		
		else if(c == 'B')
		{
			System.out.println("You Select B");
		}
		else
		{
			System.out.println("You Not Select Alphabet.");
		}
	}
}