// Switch Case Program

class Condition4
{
	public static void main(String[] args) 
	{
		int a = 10;
		switch(a)
		{
		case 1:
			System.out.println("One");
			break;
		case 2:
			System.out.println("Two");
			break;
		case 3:
			System.out.println("Three");
			break;
		default:
			System.out.println("No Not Available.");

		}
	}
}